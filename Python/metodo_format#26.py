#Primera alternativa
nombre = "Steven"

edad = 22

print("Hola {} tienes {} años".format(nombre, edad))

nombre = "Steven"; edad = 22; print("Hola {} tienes {} años".format(nombre, edad))

#Segunda alternativa
nombre = "Steven"

edad = 22

print("Hola {nombre} tienes {edad} años".format(nombre = "Steven", edad = 22))

nombre = "Steven"; edad = 22; print("Hola {nombre} tienes {edad} años".format(nombre="Steven", edad = 22))

#Tercera alternativa
nombre = "Steven"

edad = 22

print("Hola {0} tienes {1} años".format(nombre, edad))

nombre = "Steven"; edad = 22; print("Hola {0} tienes {1} años".format(nombre, edad))
