# String
titulo_curso = "Curso profesional de Python"
print(titulo_curso, type(titulo_curso))

# Int
numero_uno = 10 #puede ser numero negativo o positivo
print(numero_uno, type(numero_uno))

# Float
numero_dos = 3.14  #puede ser numero negativo o positivo
print(numero_dos, type(numero_dos))

# Bool
valor = True
print(valor, type(valor))
valor2 = False
print(valor2, type(valor2))