numero_uno = 10  #Los operadores relacionales nos permiten comparar tipos de datos 
numero_dos = 20

resultado = numero_uno > numero_dos
print(resultado)

resultado = numero_uno < numero_dos
print(resultado)

resultado = numero_uno >= numero_dos
print(resultado)

resultado = numero_uno <= numero_dos
print(resultado)

resultado = numero_uno == numero_dos
print(resultado)

resultado = numero_uno != numero_dos
print(resultado)

